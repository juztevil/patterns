import java.util.*;
class xInAStarFrame{
    public static void main(String args[]){
        Scanner scan=new Scanner(System.in);
        printThePattern(scan.nextInt());
        scan.close();
    }
    public static void printThePattern(int n){
        int rc=n+(n-1);
        for(int i=0;i<=rc;++i){
            for(int j=rc;j>-1;--j){
                if(j==i || j==rc-i){
                    if(j==i)
                    System.out.print("/");
                    else
                    System.out.print("\\");
                }
                else{
                    System.out.print("*");
                }
            }
            System.out.println();
        }
    }
}


